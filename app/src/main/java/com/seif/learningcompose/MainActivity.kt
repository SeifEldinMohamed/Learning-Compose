class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent { // has all the composables that it should draw
            Column(
                modifier = Modifier
                    .background(Color.White)
                    .fillMaxWidth()
